const { nanoid } = require('nanoid');
const books = require('./books');

const addBookHandler = (request, h) => {
  const {
    name,
    year,
    author,
    summary,
    publisher,
    pageCount,
    readPage,
    reading,
  } = request.payload;

  const id = nanoid(16);
  const finished = pageCount === readPage;
  const insertedAt = new Date().toISOString();
  const updatedAt = insertedAt;

  // validasi jika client tidak melampirkan properti name pada request body
  if (name === undefined) {
    const response = h.response({
      status: 'fail',
      message: 'Gagal menambahkan buku. Mohon isi nama buku',
    });
    response.code(400);
    return response;
  }

  // validasi jika client melampirkan nilai readPage yang lebih besar dari pageCount
  if (readPage > pageCount) {
    const response = h.response({
      status: 'fail',
      message: 'Gagal menambahkan buku. readPage tidak boleh lebih besar dari pageCount',
    });
    response.code(400);
    return response;
  }

  // menambahkan data buku
  const newBook = {
    id,
    name,
    year,
    author,
    summary,
    publisher,
    pageCount,
    readPage,
    finished,
    reading,
    insertedAt,
    updatedAt,
  };
  books.push(newBook);

  // apakah sukses menambahkan data?
  const isSuccess = books.filter((book) => book.id === id).length > 0;

  // sukses
  if (isSuccess) {
    const response = h.response({
      status: 'success',
      message: 'Buku berhasil ditambahkan',
      data: {
        bookId: id,
      },
    });

    response.code(201);
    return response;
  }

  // gagal
  const response = h.response({
    status: 'fail',
    message: 'Buku gagal ditambahkan',
  });
  response.code(500);
  return response;
};

const bookResponse = (h, filter) => {
  const response = h.response(
    {
      status: 'success',
      data: {
        books: filter.map((book) => (
          {
            id: book.id,
            name: book.name,
            publisher: book.publisher,
          })),
      },
    },
  );
  return response;
};

const getAllBookHandler = (request, h) => {
  const { reading, finished, name } = request.query;
  let filter = books;
  console.log(finished);
  console.log(name);
  console.log(reading);

  // filer name
  if (name !== undefined) {
    const search = new RegExp(name.toLowerCase(), 'i');
    filter = books.filter((book) => search.test(book.name.toLowerCase()));
    return bookResponse(h, filter);
  }

  // filter finish
  if (finished !== undefined) {
    if (finished === '1') {
      filter = books.filter((book) => book.finished === true);
    } else {
      filter = books.filter((book) => book.finished === false);
    }
    return bookResponse(h, filter);
  }

  // filter reading
  if (reading !== undefined) {
    if (reading === '1') {
      filter = books.filter((book) => book.reading === true);
    } else {
      filter = books.filter((book) => book.reading === false);
    }
    return bookResponse(h, filter);
  }

  return bookResponse(h, filter);
};

